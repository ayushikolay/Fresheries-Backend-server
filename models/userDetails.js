const mongoose = require("mongoose");

const userDetailSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    Unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  },
});

const UserDetails = new mongoose.model("UserDetails", userDetailSchema);
module.exports = UserDetails;
